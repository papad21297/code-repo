char	*ft_strlowcase(char *str)
{
	char	*ptr;

	if (str[0] == '\0')
		return (str);
	ptr = str;
	while (*ptr != '\0')
	{
		if ((*ptr >= 'A') && (*ptr <= 'Z'))
			*ptr = *ptr + 32;
		ptr++;
	}
	return (str);
}
